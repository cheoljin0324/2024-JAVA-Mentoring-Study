public class Increase {
    public static void main(String[] args){
        int a = 10;
        System.out.println("기본 변수 값:" + a);
        a++;
        System.out.println("++한 이후의 변수 값: " + a);

        int b = 20;
        System.out.println("기본 변수 값 :" + b);
        b--;
        System.out.println("-- 한 이후 변수 값 : " + b);
    }
}
